package modelo;

public class Matematica {
	
	public float somar(float v1, float v2) {
		return v1 + v2;
	}
	
	public float subtrair(float v1, float v2) {
		return v1 - v2;
	}
	
	public float multiplicar(float v1, float v2) {
		return v1 * v2;
	}
	
	public float dividir(float v1, float v2) {
		return v1 / v2;
	}

}
